class edit {
  String? id;
  String? token;

  List<String>? param;
  List<String>? newValue;

  edit({required this.id, required this.token, required this.param, required this.newValue});


  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['id'] = id;
    data['token'] = token;
    data['param'] = param;
    data['new_value'] = newValue;
    return data;
  }
  Map<String, dynamic> toJson2() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['param'] = param;
    data['new_value'] = newValue;
    return data;
  }
}
